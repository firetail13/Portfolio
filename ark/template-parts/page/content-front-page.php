			<a id="Restriction"></a>
			<h2>Restriction</h2>
			<p>Genesis: Part 1 not only restricts the riding of flying Creatures within the Genesis Simulation (with the exception of the  Astrocetus and Gasbags), but also certain special abilities of Tek Armor.</p>
			<ul>
				<li>Tek Chestpiece jetpack is disabled, both in air and in water.</li>
				<li>Tek Leggings super speed mode is disabled, both in air and in water. However, its ability to move while encumbered is still available.</li>
			</ul>
			<p>These can be disabled via server settings.</p>
			<h2>Missions</h2>
			<p>There are 176 Missions across the 5 simulated maps of Genesis. Completing missions grant Hexagons and Reward items as well as XP. Missions can be done alone or in a group and each Mission has an Alpha, Beta, and Gamma difficulty. Some missions allow you to use tamed creatures, some provide a high level creature for you to use during the mission, and some do not allow creatures to be used (Tamed creatures in the mission area will automatically be put in a temporary Cryopod on the inventory of the person who initiated the mission after the mission is over. Once these temporary  Cryopods are used they will disappear from the user's inventory.) Missions can be repeated as often as you want although they will reward significantly fewer Hexagons if they are repeated at the same difficulty level in a short period.</p>
