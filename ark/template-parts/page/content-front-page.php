			<h2>The Simulated Maps</h2>
			<hr>
			<h3>Overview</h3>
			<p>Genesis: Part 1 is a simulation across 5 distinct biomes in the form of mini maps: Bog, Arctic, Ocean, Volcanic, and Lunar. Players can teleport between these areas using the HLNA Biome Teleportation menu (using beds will drop any items making teleporting with HLN-A recommended). Players can select a biome and a cardinal direction to spawn in at any time. The player will retain any items in their inventory and any nearby tames will teleport with the player.</p>
			<h3>New Resources</h3>
			<p>Instead of Explorer Notes, you will find Glitches around the map. These Glitches can be "fixed" by the survivor to earn information from HLNA and the new in-game currency, Hexagons. Hexagons can also be earned by completing missions. Hexagons can be traded in using the HLNA menu for resources as well as for a Level 1, 2 or 3 Lootcrate (Genesis: Part 1).</p>
			<h3>New Features</h3>
			<ul class="unique">
				<li>Oxygen Vents in the lunar biome that launch players into the air allowing them to transverse the scattered asteroids.</li>
				<li>Avalanches in the Arctic and Volcanic biome that spawn on certain slopes then move downhill damaging and knocking back anything in its path.</li>
				<li>Missions across the simulations that grants graded loots, including tek items.</li>
			</ul>
			<h3>New Creatures</h3>
			<p>Every simulated biome contains a new creature, from a destructive  Magmasaur down to a cute and cuddly  Ferox with a hidden secret, the wise and sturdy  Megachelon that acts as a moveable base, the ever space-wandering  Astrocetus to be tamed, and the always watching  Bloodstalker.</p>
			<p>Moreover a brand new variant of creatures called  X-Creatures can be found across the biomes, each possessing boosted stats from their original counterpart.</p>
			<p>A new Tek Creature,  Tek Triceratops, is found in the Lunar Biome.</p>
